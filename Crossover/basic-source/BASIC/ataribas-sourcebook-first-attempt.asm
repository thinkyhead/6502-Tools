		CMP	#2		; TEST CODE =2
		BCS	@TSTOR		; BR IF POSSIBLE OR

		JSR	_INCCPC		; CODE = 0 OR 1
		JSR	_INCCPC		; INC PC BY TWO
		BNE	FAIL		; AND CONTINUE FAIL PROCESS

