		.if	BASIC_REVISION = 1
		BNE	@POP1		; BR NOT TOP OF STACK
		RTS			; TO SYNTAX CALLER
		.else
		beq	NRTS1
		.endif

@POP1:		LDA	SPC,X		; MOVE STACK PC
		STA	CPC		; TO CURRENT PC
		LDA	SPC+1,X
		STA	CPC+1

		DEX			; X=X-4
		DEX
		DEX
		DEX
		STX	STKLVL

FAIL2:		BCS	FAIL		; BR IF CALLER FAILING
		JMP	NEXT		; ELSE GO TO NEXT

