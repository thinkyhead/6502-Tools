		JSR	TSTEND
		BCS	@5
		JSR	XRNT1
		BCC	@7
		JMP	XRD3

@4:		JSR	TSTEND
		BCC	@6

@5:		JSR	INTLBF
		LDA	#0
		STA	ENTDTD
		RTS

@6:		JSR	XRNT1
		BCC	@7
		JMP	XIN0

@7:		INC	CIX
		JMP	XINA

; XPRINT - Execute PRINT Statement

XPRINT:		LDA	PTABW		; execute PRINT, ?
		STA	SCANT
		LDA	#0
		STA	COX
XPR0:		LDY	STINDEX
		LDA	(STMCUR),Y
		CMP	#CCOM		; Comma (Print TAB)
		BEQ	XPTAB
		CMP	#CCR		; End of Line
		.if	BASIC_REVISION = 1
		BEQ	XPEOL
		.else
		beq	XPXCR
		.endif
		CMP	#CEOS		; End of Statement
		.if	BASIC_REVISION = 1
		beq	XPEOL
		.else
		beq	XPXCR
		.endif
		CMP	#CSC		; Semicolon
		BEQ	XPNULL
		CMP	#CPND		; # (pound) sign
		BEQ	XPRIOD
		JSR	XLET
		JSR	ARGPOP
		DEC	STINDEX
		BIT	VTYPE
		BMI	XPR2D

		.if	BASIC_REVISION > 1
		lda	FR0+1
		cmp	#$10
		bcc	XPR1
		lda	FR0+5
		and	#$F0
		sta	FR0+5
		.endif

XPR1:		JSR	CVFASC
		LDA	#0
		STA	CIX
@1:		LDY	CIX
		LDA	(INBUFF),Y
		PHA
		INC	CIX
		JSR	XPRC
		PLA
		BPL	@1
		BMI	XPR0

XPR2D:		JSR	GSTRAD
		LDA	#0
		STA	CIX
XPR2C:		LDA	VTYPE+EVSLEN
		BNE	XPR2B
		DEC	VTYPE+EVSLEN+1
		BMI	XPR0
XPR2B:		DEC	VTYPE+EVSLEN
		LDY	CIX
		LDA	(VTYPE+EVSADR),Y
		INC	CIX
		BNE	XPR2A
		INC	VTYPE+EVSADR+1
XPR2A:		JSR	XPRC1
		JMP	XPR2C

XPTAB:		LDY	COX
		INY
		CPY	SCANT
		BCC	XPR4
		CLC
		LDA	PTABW
		ADC	SCANT
		STA	SCANT
		BCC	XPTAB
XPR4:		LDY	COX
		CPY	SCANT
		BCS	XPR4A
		LDA	#' '
		JSR	XPRC
		JMP	XPR4

		.if	BASIC_REVISION > 1
XPXCR:		jmp	XPCR
		.endif

XPRIOD:		JSR	GIOPRM
		STA	LISTDTD
		DEC	STINDEX
		JMP	XPR0
XPR4A:
XPNULL:		INC	STINDEX

		.if	BASIC_REVISION = 1
		JMP	XPR0
XPEOL:
XPEOS:		LDY	STINDEX
		DEY
		LDA	(STMCUR),Y
		CMP	#CSC
		BEQ	XPRTN
		CMP	#CCOM
		BEQ	XPRTN
		.else
		ldy	STINDEX
		lda	(STMCUR),Y
		cmp	#CCR
		beq	XPRTN
		cmp	#CEOS
		beq	XPRTN
		jmp	XPR0
		.endif

XPCR:		LDA	#CR
		JSR	XPRC1
XPRTN:		LDA	#0
		STA	LISTDTD
		RTS

XPRC:		AND	#$7F
XPRC1:		INC	COX
		JMP	PRCHAR

; XLPRINT - Print to Printer

XLPRINT:	LDA	#<PSTR		; execute LPRINT
		STA	INBUFF
		LDA	#>PSTR
		STA	INBUFF+1
		LDX	#7
		STX	LISTDTD
		LDA	#0
		LDY	#8
		JSR	SOPEN
		JSR	IOTEST
		JSR	XPRINT
